package ujian;

public class percabangan {

    public static void main(String[] args) {
        
        int bil;
        bil = 0;

        // Kondisi
        while (bil <= 20) {
            if (bil % 2 != 0) {
                System.out.println(bil);
            }
            // Peubah
            bil++;
        }
    }
    
}
